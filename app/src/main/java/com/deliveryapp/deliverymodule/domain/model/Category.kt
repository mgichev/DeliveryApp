package com.deliveryapp.deliverymodule.domain.model

import android.graphics.Color

/**
 * Категории грузов для доставки.
 * Каждая категория содержит отображаемое имя и цвет для визуального выделения.
 *
 * @property displayName Название категории
 * @property color Цвет категории в формате ARGB
 */
enum class Category(val displayName: String, val color: Int) {
    /**
     * Категория для товаров, требущих холодильного оборудования
     * Цвет: синий (#011BEA)
     */
    FRIDGE("Холодильник", Color.parseColor("#011BEA")),

    /**
     * Категория для товаров, требущих обогревательного оборудования
     * Цвет: оранжевый (#EA4701)
     */
    HEATER("Обогреватель", Color.parseColor("#EA4701")),

    /**
     * Категория для хрупких грузов
     * Цвет: светло-фиолетовый (#ACA3FF)
     */
    FRAGILE("Хрупкое", Color.parseColor("#ACA3FF")),

    /**
     * Категория для больших грузов
     * Цвет: темно-фиолетовый (#251A58)
     */
    LARGE("Крупное", Color.parseColor("#251A58")),

    /**
     * Категория для небольших грузов
     * Цвет: серый (#9D9FAF)
     */
    SMALL("Мелкое", Color.parseColor("#9D9FAF")),

    /**
     * Категория для органики
     * Цвет: зеленый (#71D19E)
     */
    ORGANIC("Органика", Color.parseColor("#71D19E")),

    /**
     * Категория для срочных доставок
     * Цвет: красный (#FF0000)
     */
    URGENT("Срочное", Color.parseColor("#FF0000"))
}